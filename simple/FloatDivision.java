package simple;

public class FloatDivision {

	public static void main(String[] args) {
		
//		Just add the decimal point to get the complete fraction
		
		
		System.out.println(23/100);
		System.out.println(23%100);
		
		System.out.println();
		System.out.println(23 / 100.0);
		System.out.println(23 % 100.00);
		
		System.out.println();
		System.out.println(22/7);
		System.out.println(22/7.);
		System.out.println(22/7.0);
		System.out.println(22/7.00);
		System.out.println(22/7.000);

	}

}
